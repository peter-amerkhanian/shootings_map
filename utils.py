def get_href(event):
    """
    Get a link to a google search of the shooting
    :param event: a Pandas df row
    :return: a string link
    """
    case = event.case + " " + event.city + " " + str(event.year)
    return f'https://www.google.com/search?q={case.replace(" ","+")}'


def get_measurements(event):
    """
    adjust the size of the icons on the map
    :param event: a Pandas df row
    :return: the shooting's size on the map
    """
    measure = event.fatalities * 3
    if measure > 30:
        measure *= .5
    if measure < 10:
        measure *= 1.5
    return float(measure)


def abbreviation_translate(state):
    """
    Replaces abbreviated state names with full name
    :param state: string of the state name, could be complete, could be abrev
    :return: string of the state in full name form
    """
    full = states.get(state.upper())
    if full:
        return full
    else:
        return state
